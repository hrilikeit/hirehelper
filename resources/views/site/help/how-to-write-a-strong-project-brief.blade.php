        <article class="article-content">
          <section class="article-section" data-reveal><h2>Begin with the current situation</h2><p>Describe the product, flow, or system as it exists today. Explain what users or team members experience right now and where the pain is showing up.</p></section><section class="article-section" data-reveal><h2>State the change you need</h2><p>Be direct about what should be different when the work is done. That could mean a launched feature, a redesigned flow, a more stable product area, or a faster internal process.</p></section><section class="article-section" data-reveal><h2>Name the constraints</h2><p>Useful briefs include time pressure, launch dates, business dependencies, internal approval realities, technical limitations, and any known scope boundaries.</p></section><section class="article-section" data-reveal><h2>Include enough context to reduce guesswork</h2><p>Share what tools, systems, stakeholders, or user groups affect the work. The goal is not to overspecify every detail. The goal is to remove avoidable ambiguity.</p></section><section class="article-section" data-reveal><h2>Keep it decision-ready</h2><p>A strong brief helps everyone answer the same questions: What matters most? What is urgent? What is flexible? What does success look like? If the brief does that, it is strong enough to move forward.</p></section>
          
          
